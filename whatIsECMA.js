// ECMAScript（エクマスクリプト）とは、JavaScriptの標準仕様を定める規格のことです。

// ***********************************************************************************************************************
// **ECMAScriptの役割**
// ***********************************************************************************************************************

// * **JavaScriptの標準化:**
//     * JavaScriptは、当初ネットスケープコミュニケーションズ社によって開発されました。しかし、ブラウザ間の互換性の問題などから、国際的な標準化が必要となりました。
//     * そこで、Ecma International（エクマ・インターナショナル）という標準化団体によって、ECMAScriptという規格が策定されました。
//     * ECMAScriptは、JavaScriptの文法や機能を標準化することで、異なるブラウザや環境でも同じように動作するJavaScriptコードを書くための基盤を提供します。

// * **ブラウザ間の互換性確保:**
//     * ECMAScriptの規格に沿ってJavaScriptを実装することで、ブラウザ間の挙動の差異を減らし、開発者はより安定したWebアプリケーションを開発できます。

// * **JavaScriptの進化を促進:**
//     * ECMAScriptは定期的に改訂され、新しい機能が追加されています。これにより、JavaScriptは常に進化し、現代のWeb開発のニーズに対応しています。


// ***********************************************************************************************************************
// **ECMAScriptとJavaScriptの関係**
// ***********************************************************************************************************************

// * **ECMAScriptは規格、JavaScriptは実装:**
//     * ECMAScriptは、JavaScriptの文法や機能を定義する規格です。
//     * JavaScriptは、ECMAScriptの規格に基づいて実装されたプログラミング言語です。
//     * つまり、ECMAScriptは設計図であり、JavaScriptはその設計図に基づいて建てられた家のような関係です。

//    →　つまりは同じ動作をしているAPIでもエンジンによって中身の処理の仕方が少し異なっていたりする。

// * **JavaScriptはECMAScriptを拡張:**
//     * JavaScriptは、ECMAScriptの規格に加えて、ブラウザ固有のAPI（DOMなど）を提供しています。
//     * そのため、JavaScriptはECMAScriptよりも機能が豊富です。



// ***********************************************************************************************************************
// **ECMAScriptのバージョン**
// ***********************************************************************************************************************

// * ECMAScriptは、年ごとに新しいバージョンがリリースされています。
//     * 例えば、「ECMAScript 2023」は2023年にリリースされたバージョンです。
// * 新しいバージョンでは、新しい構文や機能が追加され、JavaScriptの表現力や開発効率が向上しています。

// ***********************************************************************************************************************
// **ECMAScriptの重要性**
// ***********************************************************************************************************************

// * ECMAScriptは、JavaScriptの基盤となる重要な規格です。
// * ECMAScriptを理解することで、JavaScriptのより深い理解につながり、より高度なWebアプリケーション開発が可能になります。
// * ECMAScriptは、Web開発者にとって必須の知識と言えるでしょう。
